public class Registro {
    String placa;
    long horaEntrada;
    long horaSaida;
    double valorPago;

    public Registro(String placa, long horaEntrada, long horaSaida, double valorPago) {
        this.placa = placa;
        this.horaEntrada = horaEntrada;
        this.horaSaida = horaSaida;
        this.valorPago = valorPago;
    }
}
